	/**
	 * Get allowed HTML tags for SVG icons (for wp_kses).
	 *
	 * @since    1.0.0
	 * @return   array    Allowed HTML tags and attributes for SVG.
	 */
	public static function get_allowed_svg_tags() {
		return array(
			'svg'    => array(
				'class'       => true,
				'width'       => true,
				'height'      => true,
				'viewbox'     => true,
				'fill'        => true,
				'xmlns'       => true,
				'style'       => true,
				'aria-hidden' => true,
				'aria-label'  => true,
				'role'        => true,
			),
			'path'   => array(
				'd'    => true,
				'fill' => true,
			),
			'circle' => array(
				'cx'   => true,
				'cy'   => true,
				'r'    => true,
				'fill' => true,
			),
			'rect'   => array(
				'x'      => true,
				'y'      => true,
				'width'  => true,
				'height' => true,
				'fill'   => true,
			),
		);
	}

	/**
	 * Get allowed HTML tags including SVG for use with wp_kses.
	 *
	 * Merges standard post allowed tags with SVG tags.
	 * Use this when escaping HTML that contains SVG icons.
	 *
	 * @since    1.0.0
	 * @return   array    Combined allowed HTML tags.
	 */
	public static function get_allowed_html_with_svg() {
		return array_merge( wp_kses_allowed_html( 'post' ), self::get_allowed_svg_tags() );
	}

	/**
	 * Get an SVG icon.
	 *
	 * Returns escaped SVG HTML safe for direct output.
	 *
	 * @since    1.0.0
	 * @param    string $name    Icon name.
	 * @param    array  $args {
	 *     Optional. Icon rendering arguments.
	 *
	 *     @type string $class            Additional CSS classes. Default ''.
	 *     @type int    $size             Icon size in pixels. Default 20.
	 *     @type string $color            Icon color (CSS value). Default 'currentColor'.
	 *     @type string $aria_label       Accessible label. Default ''.
	 *     @type bool   $aria_hidden      Whether to hide from screen readers. Default true.
	 *     @type bool   $no_inline_style  Disable inline styles (use CSS instead). Default false.
	 * }
	 * @return   string    Escaped SVG icon HTML (safe for direct output).
	 */
	public static function get( $name, $args = array() ) {
		// Return empty if icon doesn't exist
		if ( ! isset( self::$icons[ $name ] ) ) {
			return '';
		}

		$defaults = array(
			'class'           => '',
			'size'            => 20,
			'color'           => 'currentColor',
			'aria_label'      => '',
			'aria_hidden'     => true,
			'no_inline_style' => false,
		);

		$args = wp_parse_args( $args, $defaults );

		// Build classes
		$classes = array( 'wsscd-icon', 'wsscd-icon--' . esc_attr( $name ) );
		if ( ! empty( $args['class'] ) ) {
			$classes[] = esc_attr( $args['class'] );
		}

		// Build attributes
		$size = absint( $args['size'] );
		$attributes = array(
			'class'   => implode( ' ', $classes ),
			'width'   => $size,
			'height'  => $size,
			'viewBox' => '0 0 24 24',
			'fill'    => esc_attr( $args['color'] ),
			'xmlns'   => 'http://www.w3.org/2000/svg',
		);

		// Add inline styles only if not disabled
		if ( ! $args['no_inline_style'] ) {
			$attributes['style'] = sprintf( 'display:inline-block;vertical-align:middle;flex-shrink:0;width:%dpx;height:%dpx;fill:%s', $size, $size, esc_attr( $args['color'] ) );
		}

		// Add ARIA attributes
		if ( $args['aria_hidden'] ) {
			$attributes['aria-hidden'] = 'true';
		}
		if ( ! empty( $args['aria_label'] ) ) {
			$attributes['aria-label'] = esc_attr( $args['aria_label'] );
			$attributes['role'] = 'img';
		}

		// Build attributes string
		$attr_string = '';
		foreach ( $attributes as $key => $value ) {
			if ( is_bool( $value ) ) {
				if ( $value ) {
					$attr_string .= ' ' . esc_attr( $key );
				}
			} else {
				$attr_string .= sprintf( ' %s="%s"', esc_attr( $key ), esc_attr( $value ) );
			}
		}

		// Build SVG and return escaped
		$svg = sprintf(
			'<svg%s>%s</svg>',
			$attr_string,
			self::$icons[ $name ]
		);

		return wp_kses( $svg, self::get_allowed_svg_tags() );
	}

	/**
	 * Render an SVG icon (echo).
	 *
	 * Output is pre-escaped via wp_kses in get() method with custom SVG whitelist.
	 * We cannot use wp_kses_post() here as it strips SVG tags.
	 *
	 * @since    1.0.0
	 * @param    string $name    Icon name.
	 * @param    array  $args    Icon rendering arguments. See get() for details.
	 * @return   void
	 */
	public static function render( $name, $args = array() ) {
		WSSCD_HTML_Helper::output( self::get( $name, $args ) );
	}

